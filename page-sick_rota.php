        <h3>「ロタウイルス」の予防方法 </h3>
        <p>「ロタウイルス」の感染を予防するには、ウイルスを排除し、体内に取り込まないことが大切です。周囲で「ロタウイルス」が流行している時期は、マスクの着用・手洗い・消毒などの基本的な感染対策を徹底し、おむつ交換のときには使い捨ての手袋をするようにしましょう。「ロタウイルス」はアルコール消毒に無効であるため、下痢や吐物などが付着した部位は次亜塩素酸で消毒するのもポイントです。
また、任意接種ではありますが「ロタウイルス」にはワクチンが存在し、重症化を予防する効果が期待できますので積極的な接種が推奨されています。ただし、接種可能な月齢が限られていますので、まずはかかりつけの小児科で相談しましょう。<br><br><b>〈予防のために行いたいこと〉</b></p>
        <ul id="place">
          <li>マスクの着用</li>
