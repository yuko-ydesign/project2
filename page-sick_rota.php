        <h3>「ロタウイルス」の感染経路・かかりやすい季節 </h3>
        <p>「ロタウイルス」は、糞口感染が主な感染経路です。糞口感染とは、感染者の便の中に含まれたウイルスや細菌が付着したものに他の人が触れてしまうことによって感染するものです。とくに、「ロタウイルス」はおむつのを着用した乳幼児が発症しやすいため、下痢の処理の際に手指や床、タオルなどに付着したウイルスが感染の原因になることがあります。
このため、乳幼児が集団生活する保育園などで大流行するケースも少なくありません。
また、「ロタウイルス」は年のよって流行時期が異なることもありますが、例年では1～4月頃に流行します。
