        <h3>「急性中耳炎」の症状の特徴 </h3>
        <p>「急性中耳炎」とは、のどや鼻に感染したウイルスや細菌が耳管(のどと中耳をつなぐ管)から中耳（鼓膜の内側の部分）に波及して炎症を引き起こす病気のことです。子どもは大人に比べて耳管が短く、ウイルスや細菌が入り込みやすい構造をしているため、急性中耳炎を発症しやすいのが特徴です。発症すると、耳の痛み、発熱が生じ、重症化すると中耳内に膿や浸出液が溜まると鼓膜の動きが悪くなって音が聞こえにくくなったり、音がこもって聞こえるようになります。また、鼓膜の一部が破れて中耳内の膿や浸出液が排泄される「耳だれ」が見られることも珍しくありません。<br>
「急性中耳炎」は適切な治療を行わないと、炎症が慢性化して中耳内に常に水が溜まった状態になる「滲出性中耳炎」や「慢性中耳炎」に移行します。その結果、聴力低下などの症状が長引き、言葉の発達や集中力や注意力など情緒面での成長にも遅れが生じることがあります。<br><br><b>〈特徴的な症状〉</b></p>
        <ul id="place">
          <li>耳の痛み</li>
          <li>発熱</li>
          <li>耳だれ</li></ul><br><br>


