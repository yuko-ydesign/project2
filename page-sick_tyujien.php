        <h3>「急性中耳炎」の感染経路・かかりやすい季節 </h3>
        <p>「急性中耳炎」の原因となるウイルスや細菌は多岐に渡ります。一般的には風邪をひいた後に発症することが多く、感染経路は飛沫感染と接触感染によるものが多いと考えられます。飛沫感染はウイルスや細菌が含まれる感染者の咳やくしゃみのしぶきを近くにいる人が体内に取り込んでしまうことで感染するもので、接触感染はウイルスや細菌が付着したものを他の人が触ることによって感染するものです。いずれも、感染者の唾液や鼻水、痰などが感染源になりますので、十分に注意しましょう。<br>
また、「急性中耳炎」は一年を通して発症する危険はありますが、11～4月頃の風邪やインフルエンザが流行しやすい時期に発症者が多くなります。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_tyujien.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_tyujiensp.jpg"></p>
