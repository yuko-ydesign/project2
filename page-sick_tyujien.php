        <h3>「急性中耳炎」の予防方法 </h3>
        <p>「急性中耳炎」を予防するには、一般的な風邪と同じく、マスクの着用、手洗い、手指消毒などの標準的な感染症対策を行うことが大切です。また、鼻水や痰、耳だれなどが付着したティッシュなどは速やかに破棄し、素手で触った場合には速やかに手を洗うようにしましょう。症状がある間は寝具やタオルの共有を控えるのもポイントです。<br><br><b>〈予防のために行いたいこと〉</b></p>
