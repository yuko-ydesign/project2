        <h3>「急性中耳炎」の治療方法 </h3>
        <p>軽症な「急性中耳炎」の場合には、発熱に対する解熱剤、耳の痛みに対する消炎鎮痛剤などの使用による対処療法が主体となります。また、耳だれや痰などの検査の結果、細菌が原因である場合にはその細菌に効果的な抗菌薬の投与が行われます。
これらの薬物治療を続けても中耳内に液体が溜まっているような場合には、鼓膜の一部を切って液体を排出する治療が必要になるケースもありますので、できるだけ早い段階で治療を始めることが重要です。<br><br><b>〈治療の際は〉</b></p>
