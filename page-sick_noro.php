        <h3>「ノロウイルス」の予防方法 </h3>
        <p>流行時期にはマスクの着用・手洗い・手指消毒などの基本的な感染症対策を徹底することが大切です。また、家族内に発症者がいる場合には下痢や吐物の扱いには十分に注意しましょう。「ノロウイルス」はごく少量のウイルスのみで感染してしまうため、下痢や吐物から舞い上がったウイルスに触れたり、吸い込んだりしただけで感染してしまうことがあります。便座の消毒や吐物の処理を行う時はマスクと手袋を着用しましょう。また、注意したいのは消毒液の種類です。「ノロウイルス」は一般的な消毒用アルコールが効きませんのでハイターなどの次亜塩素酸を薄めたもので消毒を行う必要があります。<br>
そして、カキなどの生食は控え、十分に火を通してから食べるようにしましょう。ウイルスは加熱すると死滅しますが、料理器具などに付着して別の食材にうつってしまうこともありますので、乳幼児いる家庭ではノロウイルスに感染する可能性のある食材は避けた方が無難と言えます。<br><br><b>〈予防のために行いたいこと〉</b></p>
        <ul id="place">
