        <p>「ノロウイルス」の感染経路は、主に経口感染です。感染者の便や吐物と共に排泄されたウイルスに他の人が触れてしまうことで感染が拡がります。また、「ノロウイルス」は食中毒の原因にもなりやすく、ウイルスが付着した食べ物やウイルスが寄生するカキなどの二枚貝を口にすることで感染することもあります。
一年を通してかかる可能性はありますが、11月から４月頃にかけて流行しやすいのが特徴です。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_noro.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_norosp.jpg"></p>
