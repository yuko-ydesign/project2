        <h3>「風疹」の感染経路・かかりやすい季節 </h3>
        <p>「風疹」の感染経路は、飛沫感染と接触感染です。飛沫感染とは、ウイルスが含まれた感染者のくしゃみや咳のしぶきを近くにいる人が口や鼻の中などに取り込んでしまうことで感染するもので、接触感染は感染者から排出されたウイルスが付着したものに触れることが原因で感染するものです。いずれにせよ、感染者の唾液や鼻水にはウイルスが含まれていますので注意が必要です。<br>
また、「風疹」は、3～7月にかけて流行することが多いですが、一年を通して感染する危険があります。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_husin.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_husinsp.jpg"></p>
