        <h3>「風疹」の症状の特徴 </h3>
        <p>「風疹」とは、いわゆる「三日はしか」と呼ばれるウイルス性の感染症です。感染後2～3週間の潜伏期間を経て、発熱、小さな赤い発疹、耳の後ろや首などのリンパ節の腫れ、目の充血などが現れます。発疹は全身に広がりますが数日できれいに消え、痕が残ることはありません。また、発症者の半数は発熱が見られないとされており、熱が出た場合でも38℃以下の微熱に止まることがほとんどです。<br>
