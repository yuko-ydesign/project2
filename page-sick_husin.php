        <h3>「風疹」の予防方法 </h3>
        <p>「風疹」を予防するには、マスクの着用や手洗い・手指消毒などの基本的な感染症対策を行うことが大切です。特に家族に感染者がいる場合には、家の中でもマスクを着用し、こまめな手洗いや消毒だけでなく、ドアノブや電気スイッチ、おもちゃなどウイルスが付着している可能性のあるものはこまめにアルコール消毒をするのもポイントです。<br>
また、「風疹」は定期接種に定められているMRワクチンを接種することで感染や重症化を防ぐことができます。一歳を迎えた頃と年長学年時に1回ずつ計2回の接種が必要ですので忘れずに受けるようにしましょう。<br><br><b>〈予防のために行いたいこと〉</b></p>
