        <p>「アデノウイルス」の感染経路は、いずれのタイプも飛沫感染と接触感染です。飛沫感染とは、「アデノウイルス」が含まれた咳やくしゃみのしぶきを近くにいる人が口や鼻から体内に取り込んでしまうことで感染するもので、接触感染とは「アデノウイルス」が付着したものを他の人が触れることで感染するものです。「アデノウイルス」は感染力が非常に強いため、集団生活の場や家庭内などで感染が拡がりやすく、発症者の唾液や目やに、便などウイルスが含まれているものの扱いには注意しなければなりません。<br>
また、「アデノウイルス」はタイプによっても異なりますが、一年を通して感染する危険があるものの6～9月頃に感染者が増えるのが特徴です。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_adeno.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_adenosp.jpg"></p>
