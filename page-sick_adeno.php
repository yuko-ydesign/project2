        <h3>「アデノウイルス」の予防方法 </h3>
        <p>「アデノウイルス」の感染を防ぐには、流行時期や身近に感染者がいるときには、マスクの着用、手洗い、手指消毒などの標準的な感染対策を行うことが大切です。特に、目やになどを拭ったタオルや寝具などを介して感染することもあるため、症状が治まるまでは共用は避けるようにしましょう。また、電気スイッチやドアノブ、おもちゃなどウイルスが付着している部位はこまめに消毒をすることも大切ですが、「アデノウイルス」はアルコールが効かないのでハイターなどの次亜塩素酸を用いるのもポイントです。<br>
そして、下痢などの症状がある場合は便を介して感染が拡がることもあるため、オムツ交換の時は手袋とマスクを着用し、入浴は最後にするなどの対策も必要です。<br><br><b>〈予防のために行いたいこと〉</b></p>
        <ul id="place">
