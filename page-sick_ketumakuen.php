        <p>「流行性角結膜炎」はアデノウイルスに感染することによって発症し、感染経路は飛沫感染と接触感染です。飛沫感染とは、ウイルスが含まれた感染者の咳やくしゃみのしぶきを近くにいる人が口や鼻から吸い込んでしまうことで感染するもので、接触感染とはウイルスが付着したものを他の人が触れることによって感染するものです。<br>
アデノウイルスは感染力が非常に強く、「流行性角結膜炎」ではウイルスが含まれた目やにや涙を擦った手やタオルなどを介した接触感染が起こりやすいのが特徴です。
また、「流行性角結膜炎」は、7～8月頃の暑い時期に流行する傾向にありますが、一年を通して感染する危険があるので注意が必要です。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_ketumakuen.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_ketumakuensp.jpg"></p>
