        <h3>「流行性角結膜炎」の予防方法 </h3>
        <p>「流行性角結膜炎」を予防するには、マスクの着用や手洗い、手指消毒などの標準的な感染対策を徹底することが重要です。また、家族に感染者がいる場合はタオルや寝具の共用、食器の使いまわしなどは避け、目やにや涙はティッシュペーパーや使い捨てのガーゼなどでふき取り、速やかに破棄するようにしましょう。<br>
そして、ウイルスは便にも含まれているため、オムツ交換の際には手袋とマスクを着用し、床や周囲への汚染に注意しましょう。<br><br><b>〈予防のために行いたいこと〉</b></p>
