        <h3>「風邪」の予防方法 </h3>
        <p>「風邪」を予防するには、細菌やウイルスなどの病原体を排除し、体内に取り込まないことが大切です。そのためには、「風邪」が流行しやすい時期はマスクを着用し、手洗いと手指消毒を徹底するようにしましょう。<br>
また、発症者が家族内にいる場合は家庭内でもマスクの着用・手洗い・手指消毒はもちろんのこと、電気スイッチやドアノブ、おもちゃなどウイルスや細菌が付着している可能性が高い部位はアルコールでこまめに消毒するのもポイントです。<br>
さらに、日ごろから規則正しい生活を心がけ、乾燥した時期は適度に加湿して喉の状態をよくするなど、「風邪」に打ち勝つ体力作りも必要となります。<br><br><b>〈予防のために行いたいこと〉</b></p>
        <ul id="place">
          <li>マスクの着用</li>
          <li>手洗い</li>
          <li>手指消毒</li>
          <li>細菌が付着した部位をアルコール消毒</li>
          <li>適度な加湿</li></ul><br><br>


