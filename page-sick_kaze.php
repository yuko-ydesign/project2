多くは対処療法のみで回復しますが、乳幼児などでは十分な水分を摂取できずに脱水症になったり、肺炎などの合併症を併発することもあり、入院治療が必要になることがあります。「風邪」だからと軽く考えず、重症化する前にできるだけ早く病院を受診して治療を行うことが大切です。<br><br><b>〈治療の際は〉</b></p>
