        <h3>「風邪」の感染経路・かかりやすい季節 </h3>
        <p>「風邪」の原因となる病原体は多々ありますが、約半数はライノウイルス原因です。いずれも飛沫感染・接触感染によって感染します。飛沫感染とは、ウイルスや細菌が含まれた発症者のくしゃみや咳のしぶきを近くにいる人が吸い込んでしまうことで感染するものです。<br>一方、接触感染とは発症者の唾液や便などと共に排泄されたウイルスや細菌が付着したものに触れてしまうことによって感染するものです。
最も多いライノウイルス感染による「風邪」は春と秋に流行しやすいのが特徴ですが、「風邪」は一年を通してかかる可能性があるため季節を問わず注意すべき感染症の一つです。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_kaze.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/seazon_kazesp.jpg"></p>
