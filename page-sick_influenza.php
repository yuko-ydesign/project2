        <h3>「インフルエンザ」の予防方法 </h3>
        <p>「インフルエンザ」の感染を防ぐには、マスクの着用、手洗い、手指消毒などの基本的な感染症対策を徹底することが何よりも大切です。また、家族内に発症者がいる場合は、家庭内でも同様の対策を行い、電気スイッチやドアノブ、おもちゃなどウイルスが付着している可能性がある場所はこまめにアルコール消毒をしましょう。
「インフルエンザ」には予防接種がありますが、感染を完全に防ぐことができません。しかし、万が一発症した場合でも重症化を予防することができますので、積極的な接種が推奨されています。<br><br><b>〈予防のために行いたいこと〉</b></p>
