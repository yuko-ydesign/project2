        <p>「インフルエンザ」は、飛沫感染や接触感染によってインフルエンザウイルスに感染することで発症します。飛沫感染とは、ウイルスが含まれた感染者の咳やくしゃみのしぶきを近くにいる人が吸い込んでしまうことで感染するものです。一方、接触感染は感染者から排出されたウイルスが付着したものに触れることで感染するものです。毎年、「インフルエンザ」は11月下旬ころから患者が現れ、1～3月頃に流行のピークを迎えた後に5月頃にかけて徐々に終息していきます。しかし、近年、温暖な地域では夏にインフルエンザが流行することもあるので注意が必要です。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_inful.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_infulsp.jpg"></p>
