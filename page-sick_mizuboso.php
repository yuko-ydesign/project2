        <h3>「水ぼうそう」の予防方法 </h3>
        <p>流行時期や周囲に感染者がいる時には、マスクの着用・手洗い・手指消毒などの基本的な感染症対策を徹底して行うことが大切です。しかし、通常のマスクでは空気感染を予防することはできないため、家族内に感染者がいる場合はこまめに室内を換気するようにしましょう。そして、ウイルスは破れた発疹にも含まれるため、寝具やタオルなどの共用は、発疹が完全に痂皮化(かさぶた化)して剥がれ落ちるまで控えるのもポイントです。<br>
