        <h3>「はしか」の予防方法 </h3>
        <p>「はしか」は空気感染するため、市販されている通常のマスクで予防することは困難です。飛沫感染や接触感染を予防する目的でマスクの着用や手洗いなどの標準的な対策を行うことも大切ですが、空気感染を防ぐことができない以上、感染してしまう危険は誰にでもあります。<br>
「はしか」にはワクチンがあり、日本ではＭＲワクチンとして1歳を迎えた頃と年長学年の計二回の接種が定期化されています。ワクチンの効果は高く、2回接種を行えば、99％で抗体ができるとされています。対象年齢になったら忘れずに接種するようにしましょう。
また、感染者と密に接した家族などは、感染者との接触後72時間以内に緊急でワクチン接種を行うことで発症を予防することができます。「はしか」の予防接種を受けたことがない人で感染者と密な接触があった場合には、できるだけ早く医師に緊急接種について相談してみましょう。<br><br><b>〈予防のために行いたいこと〉</b></p>
