        <h3>「はしか」の感染経路・かかりやすい季節 </h3>
        <p>「はしか」は空気感染・飛沫感染・接触感染など様々な感染経路によって感染します。空気感染とは、空気中を漂うウイルスを同じ空間にいる人が吸い込むことで感染するものです。一方、飛沫感染や接触感染は、ウイルスが含まれた感染者の咳やくしゃみなどのしぶきを近くにいる人が口や鼻から体内に取り込んでしまったり、ウイルスが付着したものに触れることによって感染するものです。「はしか」のウイルスは非常に感染力が強いため、ほんのわずかな時間近くにいただけで空気感染することもあり、集団生活の場や公共交通機関などを介して大流行することがあります。<br>
また、「はしか」は3～6月頃に流行することが多いですが、最近では季節を問わず海外からウイルスが持ち込まれることも多く、一年を通して感染する危険があります。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_hasika.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_hasikasp.jpg"></p>
