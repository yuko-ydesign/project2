        <h3>「はしか」の症状の特徴 </h3>
        <p>「はしか」は、38度前後の発熱やのどの痛み、咳、鼻水、目の充血などの症状が現れ、一旦熱が下がった後に再び39.5度前後の高熱と全身に赤い発疹が現れるのが特徴のウイルス感染症です。症状は非常に重く、特に発疹が出ている「発疹期」には3～4日間にかけて高熱や強いのどの痛み、咳などの症状が現れるため、乳幼児では脱水状態になることも少なくありません。また、「はしか」は肺炎や脳炎などの重篤な合併症を起こしやすく、死に至るケースもあります。<br>
