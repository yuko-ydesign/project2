        <h3>「咽頭結膜炎（プール熱）」の感染経路・かかりやすい季節 </h3>
        <p>「咽頭結膜炎（プール熱）」の感染経路は、飛沫感染と接触感染です。感染者の体内で増殖するアデノウイルスは唾液や眼脂、便、鼻水などと共に体外へ排出されます。飛沫感染はウイルスが含まれた唾液のしぶきを周囲の人が鼻や口から体内に取り込んでしまうことによって感染するもので、接触感染はウイルスが含まれた唾液や眼脂、便、鼻水などが付着したものを他の人が触れてしまうことで感染するものです。アデノウイルスは感染力が非常に高いため、集団生活の場などでは大流行することもあります。<br>
また、「咽頭結膜炎（プール熱）」は、一年を通して感染する危険がありますが、夏に流行しやすいのが特徴です。通常では6月頃から徐々に感染者が増え始め、7～8月頃にピークを迎えます。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_pool.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_poolsp.jpg"></p>
