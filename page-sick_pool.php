        <h3>「咽頭結膜炎（プール熱）」の予防方法 </h3>
        <p>「咽頭結膜炎（プール熱）」を予防するには、流行時期や身近に感染者がいる場合はマスクの着用や手洗い、手指消毒などの標準的な感染対策を行うことが大切です。特に家庭内ではタオルや寝具の共有は避け、電気スイッチやドアノブ、おもちゃなどウイルスが付着している可能性が高いものはこまめに次亜塩素酸で消毒するようにしましょう。<br>
また、ウイルスは便の中にも排出されるため、オムツ交換の際にはしっかり手袋とマスクを着用し、床などを便で汚染しないように注意しながら行いましょう。アデノウイルスはお湯などを介して感染することもあるため、入浴は最後にするのもポイントです。<br><br><b>〈予防のために行いたいこと〉</b></p>
