        <h3>「咽頭結膜炎（プール熱）」の治療方法 </h3>
        <p>「咽頭結膜炎（プール熱）」の原因となるアデノウイルスには抗ウイルス薬がないため、治療は高熱に対する解熱剤、のどなどの痛みに対する鎮痛剤、目の充血に対する目薬などの対処療法が主体となります。目薬は炎症による角膜や結膜の乾燥を防ぐほか、細菌の二次感染を防ぐために抗菌剤入りのものが使用されることもあります。<br>
また、乳児では脱水症状になることも多く、十分な水分が摂取できない場合には点滴治療が必要になることも少なくありません。<br><br><b>〈治療の際は〉</b></p>
