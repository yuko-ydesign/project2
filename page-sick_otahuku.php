        <p>「おたふくかぜ」はムンプスウイルスの飛沫感染、接触感染によって感染します。飛沫感染は、ムンプスウイルスが含まれた感染者の咳やくしゃみのしぶきを近くにいる人が鼻や口から取り込んでしまうことで感染するもので、接触感染は感染者から排出されたウイルスが付着したものを他の人が触れることで感染するものです。<br>
「おたふくかぜ」は潜伏期間が2～3週間と長いのが特徴ですが、この潜伏期間の間にも唾液や鼻水などとともにウイルスが排出されます。このため、まったく症状がない段階でも周囲に感染を拡げている可能性があるのです。その上、ムンプスウイルスは非常に感染力が強いため、3～8月頃の流行期には子どもの集団生活の場で大流行することも少なくありません。<br><br><img class="pc" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_otahuku.jpg"><img class="sp" src="<?php echo get_template_directory_uri(); ?>/assets/img/plan/sick/season_otahukusp.jpg"></p>
