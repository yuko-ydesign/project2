        <h3>「おたふくかぜ」の予防方法 </h3>
        <p>「おたふくかぜ」を予防するには、流行時期や周囲に感染者がいる時にはマスクの着用・手洗い・手指消毒などの標準的な感染症対策を徹底することが大切です。とくに家庭内に感染者がいる場合には、家の中でもマスクを着用し、電気スイッチやドアノブ、おもちゃなど唾液や鼻水が付着した可能性がある部位はこまめに消毒をすることも大切です。<br>
また、「おたふくかぜ」にはワクチンがあり、一歳から受けることができます。任意接種ではありますが、髄膜炎や難聴などの重篤な合併症のリスクを大幅に軽減することができるため、積極的な接種が勧められています。<br><br><b>〈予防のために行いたいこと〉</b></p>
